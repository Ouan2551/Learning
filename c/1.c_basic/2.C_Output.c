// new line using '\n' in code
// using '\n' 2 time creat blank line

// there have another "escape sequence" not just only '\n'
// \t => tap, \\ insert backslash char, \" insert "
#include <stdio.h>
int main()
{
    printf("\tMonday\n"); // insert tap
    printf("\\Tuesday"); // insert '\'
    printf("\n\n"); // create blank line
    printf("Wednesday\""); // insert "
    return 0;
}